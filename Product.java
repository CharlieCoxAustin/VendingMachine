
public class Product {
    String name;
    int price;
    
    public Product(String itemName, int itemPrice)
    {
        this.name = itemName;
        this.price = itemPrice;
    }
    
    public String getName()
    {
        return this.name;
    }
    
    public int getPrice()
    {
        return this.price;
    }
    
    
    
}
